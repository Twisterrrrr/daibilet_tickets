async function main() {
  const cities = await prisma.city.findMany({
    where: { name: { in: Object.keys(DESCRIPTIONS) } },
    select: { id: true, name: true },
  });
  let updated = 0;
  for (const c of cities) {
    const desc = DESCRIPTIONS[c.name];
    if (desc) {
      await prisma.city.update({
        where: { id: c.id },
        data: { description: desc },
      });
      console.log(`Обновлено: ${c.name}`);
      updated++;
    }
  }
  console.log(`\nИтого обновлено: ${updated} из ${Object.keys(DESCRIPTIONS).length}`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
